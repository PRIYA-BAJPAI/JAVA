class Datatype{
    public static void main(String[] args){
        byte a=1;
        short b=5;
        int c=5;
        float d=90.5f;
        boolean data=false;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(data);
    }
}